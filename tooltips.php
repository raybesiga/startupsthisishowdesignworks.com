	<h6>What are Wireframes?</h6>
	<p>Wireframes connect information architecture to the visual design of a website or app. Wireframes help establish functionality, and the relationships between different screen templates of a product. An iterative process, creating wireframes is an effective way to make rapid prototypes of pages, while measuring the practicality of a design concept.</p>
	<p><b>Wireframes focus on:</b></p>
	<ul>
		<li>The kinds of information displayed</li>
		<li>The range of functions available</li>
		<li>The relative priorities of the information and functions</li>
		<li>The rules for displaying certain kinds of information</li>
		<li>The effect of different scenarios on the display</li>
	</ul>
